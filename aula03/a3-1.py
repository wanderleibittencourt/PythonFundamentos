# Procedimento = não possui retorno
# Função = Existe retorno
# Parametro = é a variavel recebida em uma funcao ou procedimento
# Argumento = é o dado passado durante a chamada de um funcao ou procedimento

# Soma
def somar(numero1, numero2):
    resultado = numero1 + numero2
    print(resultado)

somar(10, 12)

# Subtração
def subtrair(numero1, numero2):
    resultado = numero1 - numero2
    print(resultado)

subtrair(12, 10)

# Multiplicação
def multiplicar(numero1, numero2):
    resultado = numero1 * numero2
    print(resultado)

multiplicar(4, 10)

# Divisão
def dividir(numero1, numero2):
    resultado = numero1 / numero2
    print(resultado)

dividir(10, 4)
